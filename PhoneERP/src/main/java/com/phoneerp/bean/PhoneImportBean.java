package com.phoneerp.bean;

public class PhoneImportBean {
	private String imeiNo;

	private String purTime;

	private String color;

	private String brand;

	private String model;

	private String purPrice;

	private String currentShop;

	private String phoneType;

	public String getImeiNo() {
		return imeiNo;
	}

	public void setImeiNo(String imeiNo) {
		this.imeiNo = imeiNo;
	}

	public String getPurTime() {
		return purTime;
	}

	public void setPurTime(String purTime) {
		this.purTime = purTime;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getPurPrice() {
		return purPrice;
	}

	public void setPurPrice(String purPrice) {
		this.purPrice = purPrice;
	}

	public String getCurrentShop() {
		return currentShop;
	}

	public void setCurrentShop(String currentShop) {
		this.currentShop = currentShop;
	}

	public String getPhoneType() {
		return phoneType;
	}

	public void setPhoneType(String phoneType) {
		this.phoneType = phoneType;
	}

}
